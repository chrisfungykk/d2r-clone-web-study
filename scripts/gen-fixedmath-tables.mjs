// Offline generator for src/sim/fixedmath-tables.ts.
//
// The sim may not call Math.sin/cos/atan at runtime (cross-engine transcendental risk +
// sim-purity gate), so the trig tables are committed as literal constants. This script is
// the ONLY place Math.sin/atan is used; run it and commit the emitted file. It also computes
// TABLES_HASH with the exact byte layout of src/sim/hash.ts's Hasher (u32 length prefix, then
// each entry as big-endian float64 bits with −0 normalized), so a corrupted table fails the
// fixedmath table-hash test.
//
//   node scripts/gen-fixedmath-tables.mjs

import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const SIN_N = 4096; // entries over [0, 2π)
const ATAN_N = 1024; // entries over [0, 1] → ATAN table has ATAN_N + 1 rows (0..ATAN_N)
const TWO_PI = Math.PI * 2;

const SIN = Array.from({ length: SIN_N }, (_, i) => Math.sin((i / SIN_N) * TWO_PI));
const ATAN = Array.from({ length: ATAN_N + 1 }, (_, i) => Math.atan(i / ATAN_N));

// FNV-1a over the tables, byte-identical to Hasher.u32 + Hasher.f64.
function hashTables(arrs) {
  let h = 0x811c9dc5 >>> 0;
  const dv = new DataView(new ArrayBuffer(8));
  const byte = (b) => {
    h = Math.imul(h ^ (b & 0xff), 0x01000193) >>> 0;
  };
  const u32 = (x) => {
    const v = x >>> 0;
    byte(v >>> 24);
    byte(v >>> 16);
    byte(v >>> 8);
    byte(v);
  };
  for (const arr of arrs) {
    u32(arr.length);
    for (const x of arr) {
      dv.setFloat64(0, x + 0, false);
      for (let i = 0; i < 8; i++) byte(dv.getUint8(i));
    }
  }
  return h >>> 0;
}

const TABLES_HASH = hashTables([SIN, ATAN]);

const body = `// GENERATED by scripts/gen-fixedmath-tables.mjs — DO NOT EDIT BY HAND.
// Committed literal trig tables (determinism.md): the sim reads these instead of calling
// Math.sin/cos/atan, so trig is engine-independent. Regenerate with:
//   node scripts/gen-fixedmath-tables.mjs

/** sin(i / ${SIN_N} · 2π) for i in [0, ${SIN_N}). */
export const SIN_TABLE: readonly number[] = [
${chunk(SIN.map(numLit))}
];

/** atan(i / ${ATAN_N}) for i in [0, ${ATAN_N}]. */
export const ATAN_TABLE: readonly number[] = [
${chunk(ATAN.map(numLit))}
];

export const SIN_N = ${SIN_N};
export const ATAN_N = ${ATAN_N};

/** FNV-1a 32-bit of [SIN_TABLE, ATAN_TABLE] (Hasher byte layout) — table-corruption guard. */
export const TABLES_HASH = 0x${TABLES_HASH.toString(16).padStart(8, "0")};
`;

function numLit(x) {
  // Number#toString gives the shortest round-tripping decimal → parses back to the same double.
  return Object.is(x, -0) ? "0" : String(x);
}

function chunk(strs) {
  const lines = [];
  for (let i = 0; i < strs.length; i += 8) {
    lines.push(`  ${strs.slice(i, i + 8).join(", ")},`);
  }
  return lines.join("\n");
}

const out = join(ROOT, "src", "sim", "fixedmath-tables.ts");
writeFileSync(out, body);
console.log(`wrote ${out}`);
console.log(
  `SIN_TABLE ${SIN.length}, ATAN_TABLE ${ATAN.length}, TABLES_HASH 0x${TABLES_HASH.toString(16).padStart(8, "0")}`,
);
